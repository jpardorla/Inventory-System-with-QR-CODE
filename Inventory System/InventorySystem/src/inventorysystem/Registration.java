    public void register() {

        try {
            String pass = jpfpass.getText();
            String repass = jpfrepass.getText();

            if (pass.equals(repass)) {
                     if (!cnn.conn.isClosed()) {
                String sql = "SELECT * FROM employees where EmployeeId=?";
                cnn.ps = cnn.conn.prepareStatement(sql);
                cnn.ps.setString(1, jtfemid.getText());
                cnn.rs = cnn.ps.executeQuery();
                if (cnn.rs.next()) {
                    
                    close();

                } else {
                    JOptionPane.showMessageDialog(null, "Invalid EmployeeId");
                    jtfemid.setText("");
                    jtfemid.requestFocus();
                    return;
                }
                Statement stmt = (Statement) cnn.conn.createStatement();
                int UserId = 0;
                int isAdmin;
                int EmployeeId = Integer.parseInt(jtfemid.getText());
                String Username = jtfuser.getText();
                String Userpassword = jpfrepass.getText();
                if (chIsAdmin.isSelected()) {

                    isAdmin = 1;

                } else {

                    isAdmin = 0;

                }

                String sql1 = "INSERT INTO users VALUES('" + UserId + "','" + EmployeeId + "','" + Username + "','" + Userpassword + "','" + isAdmin + "');";
                stmt.executeUpdate(sql1);
                jtfemid.setText(null);
                jtfuser.setText(null);
                jpfpass.setText(null);
                jpfrepass.setText(null);

                JOptionPane.showMessageDialog(rootPane, "Successfully Added Account to Database:", "Successfully Added", JOptionPane.WARNING_MESSAGE);
            } 
            } else {
                jlerrorpass1.setText("Password don't match");
            }
            
        } catch (Exception e) {

            JOptionPane.showMessageDialog(rootPane, "Connection Error", "CALL YOUR ADMINISTRATOR", JOptionPane.ERROR_MESSAGE);

        }

    }

