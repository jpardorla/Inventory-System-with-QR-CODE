    private void jtfemidKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfemidKeyPressed
        String employeeid = jtfemid.getText();
        int length = employeeid.length();

        if (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') {
            if (length <= 4) {
                jtfemid.setEditable(true);
            } else {

                jtfemid.setEditable(false);
            }
        } else {
            if (evt.getExtendedKeyCode() == KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode() == KeyEvent.VK_DELETE) {
                jtfemid.setEditable(true);
            } else {
                jtfemid.setEditable(false);
            }

        }// TODO add your handling code here:
    }//GEN-LAST:event_jtfemidKeyPressed

